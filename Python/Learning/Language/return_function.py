def add(a, b):
	print("Adding:", a, "+", b)
	return a + b

def substract(a, b):
	print("Substracting:", a, "-", b)
	return a - b

def multiply(a, b):
	print("Multiplying:", a, "*", b)
	return a * b

def divide(a, b):
	print("Dividing:", a, "/", b)
	return a / b


print("Let's do some math!")

age = add(30, 5)
height = substract(78, 4)
weight = multiply(90, 2)
lq = divide(100, 2)
print()
print(age, height, weight, lq)
